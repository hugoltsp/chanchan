package com.teles.chanchan.exception;

public class ChanchanConfigException extends ChanchanException {

	private static final long serialVersionUID = -2127835606761541741L;

	public ChanchanConfigException() {
		super();
	}

	public ChanchanConfigException(String message, Throwable cause) {
		super(message, cause);
	}

	public ChanchanConfigException(String message) {
		super(message);
	}

	public ChanchanConfigException(Throwable cause) {
		super(cause);
	}

	
}
package com.teles.chanchan.exception;

public class ChanchanInvalidMediaException extends ChanchanException {

	private static final long serialVersionUID = 1902387282803071118L;

	public ChanchanInvalidMediaException() {
		super();
	}

	public ChanchanInvalidMediaException(String message, Throwable cause) {
		super(message, cause);
	}

	public ChanchanInvalidMediaException(String message) {
		super(message);
	}

	public ChanchanInvalidMediaException(Throwable cause) {
		super(cause);
	}
	
}
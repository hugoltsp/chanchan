package com.teles.chanchan.exception;

public class ChanchanMediaWriteException extends ChanchanException {

	private static final long serialVersionUID = 7976383391884530872L;

	public ChanchanMediaWriteException() {
		super();
	}

	public ChanchanMediaWriteException(String message, Throwable cause) {
		super(message, cause);
	}

	public ChanchanMediaWriteException(String message) {
		super(message);
	}

	public ChanchanMediaWriteException(Throwable cause) {
		super(cause);
	}
	
}
package com.hugoltsp.chanchan.exception;

public class ChanchanMediaDownloadException extends ChanchanException {

	private static final long serialVersionUID = -2383240145996366767L;

	public ChanchanMediaDownloadException() {
		super();
	}

	public ChanchanMediaDownloadException(String message, Throwable cause) {
		super(message, cause);
	}

	public ChanchanMediaDownloadException(String message) {
		super(message);
	}

	public ChanchanMediaDownloadException(Throwable cause) {
		super(cause);
	}

}